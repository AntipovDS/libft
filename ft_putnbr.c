#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int a;

	if (n < 0)
	{
		ft_putchar('-');
		a = -n;
	}
	else
		a = n;
	if (a < 10)
	{
		ft_putchar(a + '0');
	}
	else
	{
		ft_putnbr(a / 10);
		ft_putchar(a % 10 + '0');
	}
}
