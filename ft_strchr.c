#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char a;

	a = (char)c;
	while (*s && *s != a)
	{
		s++;
	}
	if (*s != a)
	{
		return (NULL);
	}
	return ((char *)s);
}
